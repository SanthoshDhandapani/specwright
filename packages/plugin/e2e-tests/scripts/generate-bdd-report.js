import report from "multiple-cucumber-html-reporter";
import fs from "fs";
import path from "path";
import { createRequire } from "module";

const require = createRequire(import.meta.url);

// Read project name from nearest package.json (best-effort)
const getProjectName = () => {
  try {
    const pkg = require(path.resolve(process.cwd(), "package.json"));
    return pkg.name || "E2E Tests";
  } catch {
    return "E2E Tests";
  }
};

const generateReport = () => {
  const reportDir = "reports/cucumber-bdd";
  const jsonReportPath = path.join(reportDir, "report.json");
  const projectName = getProjectName();

  // Ensure the report directory exists
  fs.mkdirSync(path.dirname(jsonReportPath), { recursive: true });

  // Handle missing or empty JSON report
  if (!fs.existsSync(jsonReportPath)) {
    console.error(`❌ JSON report not found at: ${jsonReportPath}`);
    console.log("💡 Run tests first to generate the JSON report (pnpm test:bdd)");
    console.log("⚠️  Generating empty report as fallback...");
    fs.writeFileSync(jsonReportPath, JSON.stringify([{
      description: "No test results — run tests first",
      elements: [],
      id: "empty-report",
      keyword: "Feature",
      line: 1,
      name: "Empty Report",
      tags: [],
      type: "feature",
      uri: "empty.feature"
    }], null, 2));
  } else {
    const content = fs.readFileSync(jsonReportPath, "utf8").trim();
    if (!content || content === "[]") {
      console.warn("⚠️  JSON report is empty — generating minimal fallback report...");
      fs.writeFileSync(jsonReportPath, JSON.stringify([{
        description: "No test results available",
        elements: [],
        id: "empty-report",
        keyword: "Feature",
        line: 1,
        name: "No Test Results",
        tags: [],
        type: "feature",
        uri: "empty.feature"
      }], null, 2));
    }
  }

  console.log("📊 Generating BDD HTML report...");

  report.generate({
    jsonDir: reportDir,
    reportPath: path.join(reportDir, "html-report"),
    metadata: {
      browser: {
        name: process.env.BROWSER || "chromium",
        version: "Latest",
      },
      device: "Local test machine",
      platform: {
        name: process.platform,
        version: process.version,
      },
    },
    customData: {
      title: `${projectName} — BDD Test Report`,
      data: [
        { label: "Project", value: projectName },
        { label: "Version", value: process.env.npm_package_version || "—" },
        { label: "Environment", value: process.env.TEST_ENV || process.env.NODE_ENV || "test" },
        { label: "Generated", value: new Date().toLocaleString() },
      ],
    },
    displayDuration: true,
    displayReportTime: true,
    useCDN: false,
    pageTitle: `${projectName} — BDD Test Report`,
    reportName: "Playwright BDD Test Results",
    pageFooter: `<div><p>Generated by Specwright · playwright-bdd · multiple-cucumber-html-reporter</p></div>`,
    hideMetadata: false,
    openReportInBrowser: false,
  });

  console.log("✅ BDD HTML report generated successfully!");
  console.log(`📁 Open: ${path.resolve(reportDir, "html-report", "index.html")}`);
};

try {
  generateReport();
} catch (error) {
  console.error("❌ Error generating BDD report:", error.message);
  process.exit(1);
}
